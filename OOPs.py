# Object,class, method

# In class - we need to declare attributes(variables) and behaviour(methods[functions])

class computer:                         # Class declaration

    def config(self):                   # Method Declaration
        print('i5','4Gb','1Tb')

com1 = computer()                       # com1 - object 1
com2 = computer()                       # com2 - object 2

com1.config()
computer.config(com2)
